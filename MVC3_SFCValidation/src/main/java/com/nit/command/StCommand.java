public class StCommand {
	int sno=0;
	String sname="enter your name";
	long smob=0L;
	String email="enter your email address";
	
	public int getSno() {
		return sno;
	}
	public void setSno(int sno) {
		this.sno = sno;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public long getSmob() {
		return smob;
	}
	public void setSmob(long smob) {
		this.smob = smob;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
}
